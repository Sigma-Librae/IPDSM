function scheme = cmapMuted(n)
arguments
  n (1,1) {mustBeInteger} = 9;
end

fullScheme = [51,34,136;...
              136,204,238;...
              68,170,153;...
              17,119,51;...
              153,153,51;...
              221,204,119;...
              204,102,119;...
              136,34,85;...
              170,68,153]/256;

if n<=9
  scheme = fullScheme(1:n,:);
else
  factor = ceil(n/9);
  ExpandedScheme = repmat(fullScheme,factor,1);
  scheme = ExpandedScheme(1:n,:);
end